# 이름,국어,영어,수학,합계,평균

# 3명의 학생성적을 입력받아 stus에 모두 저장하여 출력하시오.

stu = []

name = input("학생이름을 입력하세요.")
stu.append(name)
kor = int(input("국어점수를 입력하세요."))
stu.append(kor)
eng = int(input("영어점수를 입력하세요."))
stu.append(stu)
math = int(input("수학점수를 입력하세요."))
stu.append(math)
total = kor+eng+math
avg = total/3
stu_data = [name,kor,eng,math,total,avg]
stu.append(stu_data)
# stus.append(["홍길동",100,100,100,300,100,0])
print(stus_data)


